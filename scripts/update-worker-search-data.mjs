import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const dataOutputPath = resolve(root, "apps/worker-search/public/app/data/meteorites.json");
const summaryOutputPath = resolve(root, "apps/worker-search/site/data/records.js");
const packageUrl = "https://data.nasa.gov/api/3/action/package_show?id=meteorite-landings";

const numberOrNull = (value) => {
  const number = Number(value);
  return Number.isFinite(number) ? number : null;
};

const yearFromDate = (value) => {
  const match = String(value ?? "").match(/^-?\d{1,4}/);
  return match ? Number(match[0]) : null;
};

const compactClass = (value) => String(value ?? "Unknown").trim() || "Unknown";

const broadClass = (value) => {
  const label = compactClass(value);
  const first = label.split(/[\s,/()-]+/).find(Boolean) ?? label;
  return first.length > 14 ? `${first.slice(0, 13)}...` : first;
};

const hemisphere = (lat, lon) => {
  if (lat == null || lon == null) return "No coordinates";
  const northSouth = lat >= 0 ? "Northern" : "Southern";
  const eastWest = lon >= 0 ? "Eastern" : "Western";
  return `${northSouth} / ${eastWest}`;
};

const massBucket = (mass) => {
  if (mass == null || mass <= 0) return "unknown mass";
  if (mass >= 1_000_000) return "tonne-class";
  if (mass >= 10_000) return "large";
  if (mass >= 1_000) return "hand-size";
  return "small";
};

const formatMass = (mass) => {
  if (mass == null || mass <= 0) return "unknown mass";
  if (mass >= 1_000_000) return `${(mass / 1_000_000).toFixed(1)} t`;
  if (mass >= 1000) return `${(mass / 1000).toFixed(1)} kg`;
  return `${Math.round(mass)} g`;
};

const formatCoordinate = (value, positive, negative) => {
  if (value == null) return null;
  return `${Math.abs(value).toFixed(2)}${value >= 0 ? positive : negative}`;
};

const locationLabel = (lat, lon) => {
  const latitude = formatCoordinate(lat, "N", "S");
  const longitude = formatCoordinate(lon, "E", "W");
  return latitude && longitude ? `${latitude}, ${longitude}` : "no coordinates";
};

const fieldIndexes = (columns) =>
  Object.fromEntries(
    columns
      .map((column, index) => [column.fieldName, index])
      .filter(([fieldName]) => fieldName),
  );

const toRecord = (row, indexes) => {
  const name = String(row[indexes.name] ?? "Unknown meteorite").trim();
  const id = String(row[indexes.id] ?? name).trim();
  const recclass = compactClass(row[indexes.recclass]);
  const mass = numberOrNull(row[indexes.mass]);
  const year = yearFromDate(row[indexes.year]);
  const lat = numberOrNull(row[indexes.reclat]);
  const lon = numberOrNull(row[indexes.reclong]);
  const fall = String(row[indexes.fall] ?? "Unknown").trim() || "Unknown";
  const region = hemisphere(lat, lon);
  const location = locationLabel(lat, lon);

  return {
    id: `met-${id}`,
    name,
    title: name,
    recclass,
    classGroup: broadClass(recclass),
    mass,
    fall,
    year,
    lat,
    lon,
    region,
    category: recclass,
    score: Math.round(Math.log10(Math.max(mass ?? 1, 1)) * 100 + (year ?? 0) / 100),
    tags: [fall, broadClass(recclass), massBucket(mass), year ? String(year) : "unknown year"],
    summary: `${fall} ${year ?? "unknown year"} · ${recclass} · ${formatMass(mass)} · ${location}`,
  };
};

const fetchJson = async (url) => {
  const response = await fetch(url);
  if (!response.ok) throw new Error(`${response.status} ${response.statusText} for ${url}`);
  return response.json();
};

const main = async () => {
  const packageData = await fetchJson(packageUrl);
  const resource = packageData.result.resources.find(
    (item) => item.format?.toLowerCase() === "json",
  );

  if (!resource?.url) throw new Error("Could not find the NASA JSON resource URL.");

  const source = await fetchJson(resource.url);
  const indexes = fieldIndexes(source.meta.view.columns);
  const records = source.data
    .map((row) => toRecord(row, indexes))
    .filter((record) => record.name && record.id);
  const initialRows = [...records]
    .sort((left, right) => (right.mass ?? 0) - (left.mass ?? 0) || left.name.localeCompare(right.name))
    .slice(0, 12);
  const stats = {
    categories: new Set(records.map((record) => record.classGroup)).size,
    heaviest: records.reduce((max, record) => Math.max(max, record.mass ?? 0), 0),
    records: records.length,
    regions: new Set(records.map((record) => record.region)).size,
  };

  mkdirSync(dirname(dataOutputPath), { recursive: true });
  writeFileSync(dataOutputPath, `${JSON.stringify(records)}\n`);

  mkdirSync(dirname(summaryOutputPath), { recursive: true });
  writeFileSync(
    summaryOutputPath,
    `// Generated by scripts/update-worker-search-data.mjs from NASA Meteorite Landings.\n` +
      `// Source: ${resource.url}\n` +
      `// Rows: ${records.length}\n` +
      `export const dataUrl = "/app/data/meteorites.json";\n` +
      `export const initialRows = ${JSON.stringify(initialRows)};\n` +
      `export const stats = ${JSON.stringify(stats)};\n`,
  );

  console.log(`wrote ${records.length} meteorite records to ${dataOutputPath}`);
  console.log(`wrote server summary to ${summaryOutputPath}`);
};

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
